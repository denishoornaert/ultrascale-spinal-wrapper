object PMOD0 {

  /*
   * Note: The position of the pin names in the list matters! The position is the number of the pin in the pmod interface.
   * H12 -> 0 upper part
   * E10 -> 2 upper part
   * D10 -> 4 upper part
   * C11 -> 6 upper part
   * B10 -> 1 lower part
   * E12 -> 3 lower part
   * D11 -> 5 lower part
   * B11 -> 7 lower part
   */
  val names = Seq[String]("H12", "B10", "E10", "E12", "D10", "D11", "C11", "B11")

}

